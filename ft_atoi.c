#include "libft.h"

int	ft_atoi(const char *s)
{
	int	res;
	int	neg;

	res = 0;
	neg = 1;
	while (*s && (*s == ' ' || *s == '\n' || *s == '\t'
				|| *s == '\v' || *s == '\f' || *s == '\r'))
		s++;
	if (*s == '-')
		neg = -1;
	if (*s == '-' || *s == '+')
		s++;
	while (*s && *s >= '0' && *s <= '9')
		res = res * 10 + (*s++ - 48);
	return (res * neg);
}
