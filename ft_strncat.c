#include "libft.h"

char	*ft_strncat(char *d, char *s, size_t n)
{
	int	i;
	int	g;

	i = 0;
	if (*s == '\0')
		return (0);
	while (d[i] != '\0')
		i++;
	g = 0;
	while (s[g] != '\0' && g < (int)n)
	{
		d[i + g] = s[g];
		g++;
	}
	d[i + g] = '\0';
	return (d);
}
